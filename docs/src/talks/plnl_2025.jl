# ‚ïî‚ïê‚ï° 2c99e374-2b71-4eb1-a145-154ae829d77b
plus_rule_any_children = DomainRuleNode(g, [1], [VarNode(:a), VarNode(:b)]);

# ‚ïî‚ïê‚ï° 8ae05f28-e901-4a87-9d06-7c854172003b
